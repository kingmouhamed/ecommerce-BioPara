Object.entries(folderMapping).forEach(([folderName, catSlug]) => {
    const cat = categories.find(c => c.slug === catSlug);
    const dir = `${baseDir}/${folderName}`;
    if (fs.existsSync(dir)) {
        const files = fs.readdirSync(dir).filter(f => f.endsWith('.jpg') || f.endsWith('.png') || f.endsWith('.webp') || f.endsWith('.JPG'));
        files.forEach(file => {
            const nameBase = file.split('.')[0].replace(/-/g, ' ').replace(' herb', '').replace(' oil', '').replace(' honey', '');
            const name = nameBase.split(' ').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');

            const trans = translations[name] || name;
            let nameAr = trans;
            let descAr = `منتج ${trans} عالي الجودة من الطبيعة. ممتاز لدعم الصحة والحيوية.`;

            if (catSlug === 'honey') {
                nameAr = `عسل ${trans} طبيعي`;
                descAr = `عسل ${trans} طبيعي نقي 100%، غني بالفوائد والمغذيات.`;
            } else if (catSlug === 'oils') {
                nameAr = `زيت ${trans} العضوي`;
                descAr = `زيت ${trans} العضوي النقي، مستخلص بعناية للحفاظ على فوائده.`;
            } else if (catSlug === 'herbs') {
                nameAr = `أعشاب ${trans}`;
                descAr = `أعشاب ${trans} طبيعية وعضوية، مثالية للاستخدام اليومي.`;
            } else if (catSlug === 'supplements') {
                nameAr = `مكمل ${trans}`;
                descAr = `مكمل ${trans} الممتاز لدعم صحتك العامة وتعزيز نشاطك اليومي.`;
            }

            finalProducts.push({
                id: pid++,
                name: name,
                name_ar: nameAr,
                slug: `${cat.slug}-${file.split('.')[0]}`,
                description: `Premium ${name} for your health and wellbeing. Made with 100% natural ingredients.`,
                description_ar: descAr,
                price: Math.floor(Math.random() * (250 - 45) + 45),
                currency: 'MAD',
                stock: Math.floor(Math.random() * 50) + 10,
                images: [`/images/${folderName}/${file}`],
                image_url: `/images/${folderName}/${file}`,
                category_id: cat.id,
                is_active: true,
                featured: Math.random() > 0.5,
                created_at: new Date().toISOString(),
                updated_at: new Date().toISOString()
            });
        });
    }
});

