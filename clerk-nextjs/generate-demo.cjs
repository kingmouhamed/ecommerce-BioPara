    { id: 3, slug: 'oils', name: 'Medicinal Oils', name_ar: 'زيوت علاجية', description: 'Pure medicinal & essential oils', description_ar: 'زيوت علاجية وعطرية نقية', image: '/images/categories/oils.jpg', created_at: new Date().toISOString(), updated_at: new Date().toISOString() },
    { id: 4, slug: 'honey', name: 'Natural Honey', name_ar: 'عسل طبيعي', description: '100% pure natural honey', description_ar: 'عسل طبيعي 100%', image: '/images/categories/honey.jpg', created_at: new Date().toISOString(), updated_at: new Date().toISOString() }
