// --- GENERATE SUPABASE SQL SEED FILE ---
const escapeSql = (str) => typeof str === 'string' ? str.replace(/'/g, "''") : str;
const escapeArray = (arr) => `ARRAY[${arr.map(a => `'${escapeSql(a)}'`).join(',')}]`;

let sqlContent = `-- ==========================================
-- BioPara: FULL Products Table Setup & Seed Data
-- Run this script in the Supabase SQL Editor
-- ==========================================

-- 1. Drop existing tables to ensure new columns are created
DROP TABLE IF EXISTS public.products CASCADE;
DROP TABLE IF EXISTS public.categories CASCADE;

-- 2. Create the categories table
CREATE TABLE public.categories (
    id bigint generated by default as identity primary key,
    name text not null,
    name_ar text,
    slug text not null unique,
    description text,
    description_ar text,
    image text,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null,
    updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 3. Create the products table
CREATE TABLE public.products (
    id bigint generated by default as identity primary key,
    name text not null,
    name_ar text,
    slug text not null unique,
    description text,
    description_ar text,
    price numeric not null,
    currency text default 'MAD' not null,
    stock integer default 0 not null,
    images text[] default '{}'::text[],
    category_id bigint references public.categories(id) on delete cascade,
    is_active boolean default true not null,
    featured boolean default false not null,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null,
    updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 4. Turn ON ROW LEVEL SECURITY
ALTER TABLE public.products ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.categories ENABLE ROW LEVEL SECURITY;

-- 5. Create Policies
DROP POLICY IF EXISTS "Enable read access for all users" ON public.products;
DROP POLICY IF EXISTS "Enable read access for all users" ON public.categories;

CREATE POLICY "Enable read access for all users" 
ON public.products FOR SELECT USING ( true );

CREATE POLICY "Enable read access for all users" 
ON public.categories FOR SELECT USING ( true );

-- 6. Seed Categories
INSERT INTO public.categories (id, name, name_ar, slug, description, description_ar, image)
VALUES
`;

const catValues = categories.map(c =>
    `    (${c.id}, '${escapeSql(c.name)}', '${escapeSql(c.name_ar)}', '${escapeSql(c.slug)}', '${escapeSql(c.description)}', '${escapeSql(c.description_ar)}', '${escapeSql(c.image)}')`
);
sqlContent += catValues.join(',\n') + '\nON CONFLICT (slug) DO NOTHING;\n\n';

sqlContent += '-- 7. Seed Products\n';
sqlContent += 'INSERT INTO public.products (name, name_ar, slug, description, description_ar, price, currency, stock, images, category_id, is_active, featured)\nVALUES\n';

const prodValues = finalProducts.map(p =>
    `    ('${escapeSql(p.name)}', '${escapeSql(p.name_ar)}', '${escapeSql(p.slug)}', '${escapeSql(p.description)}', '${escapeSql(p.description_ar)}', ${p.price}, '${escapeSql(p.currency)}', ${p.stock}, ${escapeArray(p.images)}::text[], ${p.category_id}, ${p.is_active}, ${p.featured})`
);

sqlContent += prodValues.join(',\n') + '\nON CONFLICT (slug) DO NOTHING;\n\n-- Done!\n';

fs.writeFileSync('C:/Users/msi/Desktop/Ecommerce BioPara/clerk-nextjs/supabase_seed_products.sql', sqlContent, 'utf8');
console.log('Successfully wrote ' + finalProducts.length + ' products to supabase_seed_products.sql');

